package StringPrograms;

public class Program25_repeat {

	public static void main(String[] args) {
		String s1 = new String("Hello");
		String s2 = s1.repeat(3);
		/*
		 * After execution of this line,
		 * we are executing,
		 * s2 = HelloHelloHello
		 * 
		 * stringObj.repeat(n) : It will return a String after execution.
		 */
		System.out.println(s2);

	}

}
