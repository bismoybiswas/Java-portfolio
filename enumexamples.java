package JavaBasic;

public class enumexamples {
	public enum Color {RED, GREEN, BLUE};
	public static void main(String[] args) {
		Color c = Color.RED;
		System.out.println("The value of color "+c);
		c = Color.GREEN;
		System.out.println("The value of color "+c);
		c = Color.BLUE;
		System.out.println("The value of color "+c);

	}

}
